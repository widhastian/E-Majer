package com.pina.emajer_v2.Model.Mading;

public class MadingData {
    String id_mading;
    String id_kelas;
    String jenis_mading;
    String deskripsi_mading;
    String tgl_pembagian;

    public String getId_mading() {
        return id_mading;
    }

    public void setId_mading(String id_mading) {
        this.id_mading = id_mading;
    }

    public String getId_kelas() {
        return id_kelas;
    }

    public void setId_kelas(String id_kelas) {
        this.id_kelas = id_kelas;
    }

    public String getJenis_mading() {
        return jenis_mading;
    }

    public void setJenis_mading(String jenis_mading) {
        this.jenis_mading = jenis_mading;
    }

    public String getDeskripsi_mading() {
        return deskripsi_mading;
    }

    public void setDeskripsi_mading(String deskripsi_mading) {
        this.deskripsi_mading = deskripsi_mading;
    }

    public String getTgl_pembagian() {
        return tgl_pembagian;
    }

    public void setTgl_pembagian(String tgl_pembagian) {
        this.tgl_pembagian = tgl_pembagian;
    }
}
